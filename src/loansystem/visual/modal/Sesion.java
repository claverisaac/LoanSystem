    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
    //Conectarse a la BD
        Conexion con = new Conexion("root", "159951", "loan_system", "localhost");
        boolean conexion_abierta = con.abrirConeccion();
      
        //Verificamos si la conexion esta abierta
        if (conexion_abierta) { //si hay conexion, verificar los datos del usuario (login, password)
            String nombre_cuenta, pass = "";

            nombre_cuenta = this.txtLogin.getText();
            pass = this.txtPass.getText();
            
             if (nombre_cuenta.length() != 0 & pass.length() != 0) {
                UsuarioEntidad userEntidad;
                UsuarioDAO userDAO = new UsuarioDAO(con.getCon());
                userEntidad = userDAO.obtenerXUsuarioPass(nombre_cuenta, pass);
                
                if(userEntidad != null){
                    if(userEntidad.getEstado() == 1)
                     {
                                            
                     // obtener informacion personal del usuario
                         
                      PersonalEntidad persoEnti;
                      PersonalDAO persoDAO = new PersonalDAO(con.getCon());
                      persoEnti = persoDAO.obtenerXPersona(userEntidad.getIdPersona());
                         
                      //Obtener nombres y apellidos
                      String nombres, apellidos, nom_completo;
                      nombres = persoEnti.getNombres();
                      apellidos = persoEnti.getApellidos();
                     
                      nom_completo=nombres.concat(" ").concat(apellidos);
                      //nom_completo = nom_completo.concat(" ").concat(metodo.ObtenerPrimeraPalabra(apellidos));
                       
                                        
                        //Principal prin = new Principal(con,nom_completo, userEntidad.getLogin());
                        Principal prin = new Principal(con);
                        
                        prin.setVisible(true);
                        this.dispose();}
                    else{
                        JOptionPane.showMessageDialog(null, "Lo Sentimos, Su cuenta se encuentra inactiva", "Loan System", JOptionPane.ERROR_MESSAGE);
                        }}
                else{
                    JOptionPane.showMessageDialog(null, "Los datos digitados no corresponden a ninguna cuenta de usuario", "Loan System", JOptionPane.ERROR_MESSAGE);
                }}
             else{
              JOptionPane.showMessageDialog(null, "Debe ingresar los datos de su cuenta de usuario para iniciar sesión", "Loan System", JOptionPane.ERROR_MESSAGE);
             }
                }
       
        
    }//GEN-LAST:event_btnIniciarActionPerformed

