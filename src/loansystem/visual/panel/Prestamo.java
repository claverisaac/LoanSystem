    private int calcularDiasPlazo(String plazo, int cantidad) {
        int dias = 0;

        switch (plazo) {
            case "Dia":
                dias = 1;
                break;

            case "Semana":
                dias = 7;
                break;

            case "Mes":
                dias = 30;
                break;

            case "AÃ±o":
                dias = 365;
                break;
        }

        dias = dias * cantidad;

        return dias;
    }

