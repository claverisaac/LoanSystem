    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        PersonalDAO daoP = new PersonalDAO(con.getCon());

        if (txtNombres.getText().length() < 0 || txtNombres.getText().equals("")
                || txtApellidos.getText().length() < 0 || txtApellidos.getText().equals("")
                || txtCedula.getText().length() < 0 || txtCedula.getText().equals("   -      -     ")
                || cboCargo.getSelectedItem().toString().length() < 0
                || cboSexo.getSelectedItem().toString().length() < 0) {
            JOptionPane.showMessageDialog(this, "Lo sentimos, debe ingresar datos obligatorios!!", "Catalogo de Personal",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            if (nuevo) {

                int id = daoP.insertarPersonal(setDatos());
                if (id > 0) {

                    //    clieEnti = clieDAO.obtenerUltimoIdInsertado();
                    txtId2.setText(util.completarCerosID(id, 6));

                    JOptionPane.showMessageDialog(this, "Personal registrado exitosamente!!", "Catalogo de Personal", JOptionPane.INFORMATION_MESSAGE);
                    deshabilitar_form();
                    btnSave.setEnabled(false);
                    nuevo = false;
                    entiPerso = daoP.obtenerTodosPersonas();
                    cargarTablaPersonal();
                } else {
                    JOptionPane.showMessageDialog(this, "Lo sentimos no se regitró la persona!!", "Catalogo de Personal", JOptionPane.ERROR_MESSAGE);
                }

            }
        }

        if (modificar) {
            System.out.println("*********************Entra la if de modificar");
            int id = Integer.parseInt(txtId2.getText());
            save = daoP.updatePersonal(setDatos(), id);
            if (save) {
                JOptionPane.showMessageDialog(this, "Persona actualizada exitosamente!!", "Catalogo de Clientes", JOptionPane.INFORMATION_MESSAGE);
                deshabilitar_form();
                btnSave.setEnabled(false);
                modificar = false;
                entiPerso = daoP.obtenerTodosPersonas();
                cargarTablaPersonal();;
            }

        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private PersonalEntidad setDatos() {
        PersonalEntidad personal;
        personal = new PersonalEntidad();
        personal.setApellidos(txtApellidos.getText());
        personal.setNombres(txtNombres.getText());
        personal.setNumCedula(txtCedula.getText());
        personal.setIdEmpresa(1);
        if (cboSexo.getSelectedItem().equals("FEMENINO")) {
            personal.setSexo("FEMENINO");
        } else {
            personal.setSexo("MASCULINO");
        }

        String cargo = String.valueOf(cboCargo.getSelectedItem());
        CargoEntidad car;
        CargoDAO da = new CargoDAO(con.getCon());
        car = da.obtenerCargoXnombre(cargo);

        personal.setIdCargo(car.getIdCargo());

        return personal;

    }

