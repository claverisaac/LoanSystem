    public void cargarTablaPersonal() {
        if (entiPerso != null) {
            daoCargo = new CargoDAO(con.getCon());

            util.limpiarTabla(tabPersonal);
            for (int i = 0; i < entiPerso.size(); i++) {
                util.agregarFila(tabPersonal);
                tabPersonal.setValueAt(entiPerso.get(i).getIdPersona(), i, 0); //ID
                tabPersonal.setValueAt(entiPerso.get(i).getNombres(), i, 1); //nombres
                tabPersonal.setValueAt(entiPerso.get(i).getApellidos(), i, 2); //apellidios
                tabPersonal.setValueAt(entiPerso.get(i).getNumCedula(), i, 3); //cedula
                tabPersonal.setValueAt(entiPerso.get(i).getSexo(), i, 4); //sexo
                cargo = daoCargo.obtenerCargo(entiPerso.get(i).getIdCargo());
                tabPersonal.setValueAt(cargo.getCargo(), i, 5); //cargo
            }
        }
    }
